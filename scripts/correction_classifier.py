def main():
    import argparse

    parser = argparse.ArgumentParser(
        description="Classify user messages in Claude Code session transcripts"
    )
    subparsers = parser.add_subparsers(dest="command")

    # classify: classify a single session file
    classify_parser = subparsers.add_parser("classify", help="Classify messages in a session file")
    classify_parser.add_argument("filepath", help="Path to JSONL session file")
    classify_parser.add_argument(
        "--session-type", choices=["interactive", "subagent"], default="interactive",
        help="Session type (default: interactive)"
    )
    classify_parser.add_argument("--json", action="store_true", help="Output as JSON")

    # sample: sample and classify from corpus
    sample_parser = subparsers.add_parser("sample", help="Sample and classify from corpus")
    sample_parser.add_argument("--interactive-dir", required=True, help="Interactive sessions directory")
    sample_parser.add_argument("--subagent-dir", required=True, help="Subagent sessions directory")
    sample_parser.add_argument("--n", type=int, default=500, help="Samples per session type")
    sample_parser.add_argument("--output", required=True, help="Output JSON file path")
    sample_parser.add_argument("--seed", type=int, default=42, help="Random seed")

    # validate: generate validation sample
    validate_parser = subparsers.add_parser("validate", help="Generate validation sample")
    validate_parser.add_argument("--labeled-file", required=True, help="Labeled messages JSON file")
    validate_parser.add_argument("--n-interactive", type=int, default=50)
    validate_parser.add_argument("--n-subagent", type=int, default=50)
    validate_parser.add_argument("--output", required=True, help="Output validation file")

    # stats: print classification statistics
    stats_parser = subparsers.add_parser("stats", help="Print classification statistics")
    stats_parser.add_argument("labeled_file", help="Labeled messages JSON file")

    args = parser.parse_args()

    if args.command == "classify":
        results = classify_session(args.filepath, args.session_type)
        if args.json:
            print(json.dumps([r.to_dict() for r in results], indent=2))
        else:
            for r in results:
                print(f"[{r.label:20s}] (conf={r.confidence:.2f}) {r.raw_text[:120]}")

    elif args.command == "sample":
        print(f"Sampling {args.n} messages from interactive sessions...")
        interactive = sample_messages(args.interactive_dir, "interactive", args.n, args.seed)
        print(f"  Got {len(interactive)} interactive messages")

        print(f"Sampling {args.n} messages from subagent sessions...")
        subagent = sample_messages(args.subagent_dir, "subagent", args.n, args.seed)
        print(f"  Got {len(subagent)} subagent messages")

        all_messages = [m.to_dict() for m in interactive + subagent]
        os.makedirs(os.path.dirname(args.output), exist_ok=True)
        with open(args.output, "w") as f:
            json.dump(all_messages, f, indent=2)
        print(f"Saved {len(all_messages)} labeled messages to {args.output}")

        # Print stats
        from collections import Counter
        print("\n=== Interactive ===")
        counts = Counter(m.label for m in interactive)
        for label, count in sorted(counts.items()):
            print(f"  {label:20s}: {count:5d} ({count/len(interactive)*100:5.1f}%)")

        print("\n=== Subagent ===")
        counts = Counter(m.label for m in subagent)
        for label, count in sorted(counts.items()):
            print(f"  {label:20s}: {count:5d} ({count/len(subagent)*100:5.1f}%)")

    elif args.command == "validate":
        with open(args.labeled_file) as f:
            data = json.load(f)

        interactive = [ClassifiedMessage(**d) for d in data if d["session_type"] == "interactive"]
        subagent = [ClassifiedMessage(**d) for d in data if d["session_type"] == "subagent"]

        val_interactive = select_validation_sample(interactive, args.n_interactive)
        val_subagent = select_validation_sample(subagent, args.n_subagent)

        validation = [m.to_dict() for m in val_interactive + val_subagent]
        os.makedirs(os.path.dirname(args.output), exist_ok=True)
        with open(args.output, "w") as f:
            json.dump(validation, f, indent=2)
        print(f"Saved {len(validation)} validation samples to {args.output}")

    elif args.command == "stats":
        with open(args.labeled_file) as f:
            data = json.load(f)

        from collections import Counter

        for stype in ["interactive", "subagent"]:
            msgs = [d for d in data if d["session_type"] == stype]
            counts = Counter(d["label"] for d in msgs)
            print(f"\n=== {stype} ({len(msgs)} messages) ===")
            for label, count in sorted(counts.items()):
                avg_conf = sum(d["confidence"] for d in msgs if d["label"] == label) / max(count, 1)
                print(f"  {label:20s}: {count:5d} ({count/max(len(msgs),1)*100:5.1f}%) avg_conf={avg_conf:.3f}")

    else:
        parser.print_help()


if __name__ == "__main__":
    main()
